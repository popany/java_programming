package org.example.builder_pattern;

public class FastFood {
    String stapleFood;
    String drink;

    public String getStapleFood() {
        return stapleFood;
    }

    public String setStapleFood(String stapleFood) {
        return this.stapleFood = stapleFood;
    }

    public String getDrink() {
        return drink;
    }

    public String setDrink(String drink) {
        return this.drink = drink;
    }
}
