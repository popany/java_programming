package org.example.builder_pattern;

public class FoodContainers {
    String stapleFoodContainer;
    String drinkContainer;

    public String getStapleFoodContainer() {
        return stapleFoodContainer;
    }

    public String setStapleFoodContainer(String stapleFoodContainer) {
        return this.stapleFoodContainer = stapleFoodContainer;
    }

    public String getDrinkContainer() {
        return drinkContainer;
    }

    public String setDrinkContainer(String drinkContainer) {
        return this.drinkContainer = drinkContainer;
    }
}
